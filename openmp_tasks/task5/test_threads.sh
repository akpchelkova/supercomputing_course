#!/bin/bash
echo "исследование разного количества потоков (2000x2000):"
echo "===================================================="

# тестируем производительность при разном количестве потоков
for threads in 2 4 8 16; do
    echo "--- $threads потоков ---"
    # запускаем программу с фиксированным размером матрицы и разным числом потоков
    OMP_NUM_THREADS=$threads ./special_matrices 2000
    echo ""
done
