#!/bin/bash
echo "исследование разных размеров матриц (4 потока):"
echo "================================================"

# тестируем производительность для разных размеров матриц
for size in 500 1000 2000 5000; do
    echo "--- размер: ${size}x${size} ---"
    # запускаем программу с фиксированным количеством потоков и разным размером матрицы
    OMP_NUM_THREADS=4 ./special_matrices $size
    echo ""
done
