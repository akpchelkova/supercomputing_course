#!/bin/bash
echo "эксперименты с разным количеством потоков (1M элементов):"
echo "========================================================="

# тестируем производительность при разном количестве потоков
for threads in 1 2 4 8 16; do
    echo "--- $threads потоков ---"
    # запускаем программу с фиксированным размером векторов и разным числом потоков
    OMP_NUM_THREADS=$threads ./dot_product 1000000
    echo ""
done
