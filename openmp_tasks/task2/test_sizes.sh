#!/bin/bash
echo "сравнение разных размеров векторов (4 потока):"
echo "=============================================="

# тестируем производительность для разных размеров векторов
for size in 1000 10000 100000 1000000 10000000; do
    echo "--- размер: $size ---"
    # запускаем программу с фиксированным количеством потоков и разным размером векторов
    OMP_NUM_THREADS=4 ./dot_product $size
    echo ""
done
