#!/bin/bash
echo "сравнение разных размеров матрицы (4 потока):"
echo "============================================="

# тестируем производительность для разных размеров квадратных матриц
for size in 100 500 1000 2000 5000; do
    echo "--- размер: ${size}x${size} ---"
    # запускаем программу с фиксированным количеством потоков и разным размером матрицы
    OMP_NUM_THREADS=4 ./matrix_min_max $size $size
    echo ""
done
