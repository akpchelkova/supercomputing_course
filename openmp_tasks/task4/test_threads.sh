#!/bin/bash
echo "эксперименты с разным количеством потоков (1000x1000 матрица):"
echo "=============================================================="

# тестируем производительность при разном количестве потоков
for threads in 1 2 4 8 16; do
    echo "--- $threads потоков ---"
    # запускаем программу с фиксированным размером матрицы и разным числом потоков
    OMP_NUM_THREADS=$threads ./matrix_min_max 1000 1000
    echo ""
done
