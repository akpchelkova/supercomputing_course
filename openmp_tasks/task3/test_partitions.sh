#!/bin/bash
echo "сравнение разного количества разбиений (4 потока):"
echo "=================================================="

# тестируем точность и производительность для разного количества разбиений
for n in 1000000 10000000 100000000 500000000; do
    echo "--- разбиений: $n ---"
    # запускаем программу с фиксированным количеством потоков и разным числом разбиений
    OMP_NUM_THREADS=4 ./integral $n
    echo ""
done
