#!/bin/bash
echo "сравнение разных размеров массива (4 потока):"
echo "============================================="

# тестируем производительность для разных размеров массива
for size in 1000 10000 100000 1000000 10000000; do
    echo "--- размер: $size ---"
    # запускаем программу с фиксированным количеством потоков и разным размером массива
    OMP_NUM_THREADS=4 ./min_max $size
    echo ""
done
