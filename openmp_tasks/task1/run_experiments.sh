#!/bin/bash
echo "эксперименты с разным количеством потоков:"
echo "=========================================="

# тестируем производительность при разном количестве потоков
for threads in 1 2 4 8 16; do
    echo "--- $threads потоков ---"
    # запускаем программу с фиксированным размером массива и разным числом потоков
    OMP_NUM_THREADS=$threads ./min_max 1000000
    echo ""
done
